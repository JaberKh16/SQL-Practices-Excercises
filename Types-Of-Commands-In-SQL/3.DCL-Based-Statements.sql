/*
	SQL DCL Based Commands
	======================
	-- DCL stands for Data Control Langauge which is used to control the data
	   depending on what settings or privileges we want to give on data.

		
		DCL Based Statement
		-------------------
		
		a) GRANT statement
		-------------------
		GRANT statement is used to give user access privileges to a database.
		Syntax-
				GRANT SELECT, UPDATE ON MY_TABLE TO SOME_USER, ANOTHER_USER

		b) REVOKE statement
		-------------------
		REVOKE statment is used to revoke some access privileges to a database from the user.
		Syntax-
				REVOKE SELECT, UPDATE ON MY_TABLE FROM USER1, USER2



*/
